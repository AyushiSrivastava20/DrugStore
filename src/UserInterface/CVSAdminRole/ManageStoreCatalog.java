    private void storeSearchJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_storeSearchJButtonActionPerformed
        // TODO add your handling code here:
        String id = searchStoreJTextField.getText();
        Store store = storeCatalog.searchStore(id);
        if(store==null){
            JOptionPane.showMessageDialog(null,"Please enter a proper Name");
            return;
        }
            ViewStore vs = new ViewStore(userProcessContainer,store);
            userProcessContainer.add("ViewStore", vs);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        
    }//GEN-LAST:event_storeSearchJButtonActionPerformed


