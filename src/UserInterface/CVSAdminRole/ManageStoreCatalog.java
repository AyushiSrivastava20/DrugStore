    private void storeViewJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_storeViewJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = storeJTable.getSelectedRow();
        
        if(selectedRow>=0){
            Store st = (Store)storeJTable.getValueAt(selectedRow,0);
            ViewStore vs = new ViewStore(userProcessContainer,st);
            userProcessContainer.add("ViewStore", vs);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            
    }                                                

        else{
             JOptionPane.showMessageDialog(null,"Please Select a row from the table first","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_storeViewJButtonActionPerformed

    private void deleteStoreJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteStoreJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = storeJTable.getSelectedRowCount();
        if(selectedRow>0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"WOULD YOU LIKE TO DELETE THE STORE?","Warning", dialogButton);
            if(dialogResult==JOptionPane.YES_OPTION){
                Store ss = (Store)storeJTable.getValueAt(selectedRow,0);
                storeCatalog.removeStore(ss);
                populateTable();
                
            }
        
        }else{
            
            JOptionPane.showMessageDialog(null,"Please Select a row from the table first","WARNING", JOptionPane.WARNING_MESSAGE);
            
        }
    }//GEN-LAST:event_deleteStoreJButtonActionPerformed

