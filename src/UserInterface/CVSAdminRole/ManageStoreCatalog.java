    public void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) storeJTable.getModel();
        model.setRowCount(0);
        for (Store s : storeCatalog.getStoreList()) {
            Object row[] = new Object[3];
            row[0] = s;
            row[1] = s.getStoreId();
            row[2] = s.getAddress();
            model.addRow(row);
    }
    }
    
